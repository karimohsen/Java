        // TODO add your handling code here:
        java.awt.EventQueue.invokeLater(new Runnable() {
            public void run() {
                FormatDialog dialog = new FormatDialog(new javax.swing.JFrame(), true);
                dialog.addWindowListener(new java.awt.event.WindowAdapter() {
                    public void windowClosing(java.awt.event.WindowEvent e) {
                        System.exit(0);
                    }
                });
                dialog.setVisible(true);
                if(dialog.Apply){
                    Font f = new Font (dialog.textformateselected,Font.BOLD,dialog.formatesize);
                    jTextArea1.setFont(f);
                    if(dialog.color.equals("RED"))
                    jTextArea1.setForeground(Color.red);
                    else if(dialog.color.equals("YELLOW"))
                    jTextArea1.setForeground(Color.yellow);
                    else if(dialog.color.equals("GREEN"))
                    jTextArea1.setForeground(Color.green);
                    else if(dialog.color.equals("BLUE"))
                    jTextArea1.setForeground(Color.blue);
                    dialog.dispose();
                }

            }
        });
