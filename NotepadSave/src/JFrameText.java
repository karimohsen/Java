    public void fileinputwrite(File f) throws IOException{
        try {
            FileInputStream fis = new FileInputStream(f);
            int size = fis.available();
            byte [] b=new byte[size];
            fis.read(b);
            jTextArea1.setText(new String(b));
            fis.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(JFrameText.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    public void fileinputwrite2(File f) throws IOException{

            FileReader fr = new FileReader(f);
           
            
            int count=(int)f.length();
            count /=2;
            char [] c=new char[count];
            fr.read(c);
            jTextArea1.setText(new String(c));
            fr.close();

    }
    public void fileinputwrite3(File f) throws IOException  {
        try {
        FileInputStream  fis = new FileInputStream(f);
        DataInputStream dis = new DataInputStream(fis);
        jTextArea1.setText(dis.readUTF());
        dis.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(JFrameText.class.getName()).log(Level.SEVERE, null, ex);
        }
        

    }
